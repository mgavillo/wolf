#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long n;

	n = nb;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = n * (-1);
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
}
